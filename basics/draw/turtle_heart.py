import turtle

turtle.color("brown", "green")
turtle.shape("turtle")
turtle.speed(2)

turtle.begin_fill()

turtle.left(45)
turtle.forward(50)
turtle.left(90)
turtle.forward(25)
turtle.left(90)
turtle.forward(25)
turtle.right(90)
turtle.forward(25)
turtle.left(90)
turtle.forward(25)
turtle.left(90)
turtle.forward(50)

turtle.end_fill()

turtle.done()
